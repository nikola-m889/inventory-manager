    void table_load() {
        
        try{
            pst=con.prepareStatement("select * from inventory1");
            rs=pst.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (SQLException ex) {
            Logger.getLogger(InventoryManager.class.getName()).log(Level.SEVERE, null, ex);
        }
        
   }
    
    
    
    

