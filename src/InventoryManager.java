    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        
        String itemName = txtname.getText();
        String itemPrice = txtprice.getText();
        String itemQuantity = txtquantity.getText();
        String itemCategory = txtcategory.getText();
        String itemDescription = txtdescription.getText();
        
        try{
            pst = con.prepareStatement("insert into inventory1(name,price,quantity,category,description)values(?,?,?,?,?)");
            pst.setString(1,itemName);
            pst.setString(2,itemPrice);
            pst.setString(3,itemQuantity);
            pst.setString(4, itemCategory);
            pst.setString(5,itemDescription);
            int utable=pst.executeUpdate();
            
            if(utable==1){
                print();
                table_load();
                JOptionPane.showMessageDialog(this,"Item added to inventory");
            }
            else {
                JOptionPane.showMessageDialog(this,"error");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(InventoryManager.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

